function partOne() {
    const mapRows = input.split("\n");
    const rowLength = mapRows[0].length;

    // (0, 0) is in the top left corner, starting position
    let treesEncountered = 0;
    let xPos = 0;
    for (let yPos = 0; yPos < mapRows.length; yPos++) {
        if (mapRows[yPos].substr(xPos % rowLength, 1) == "#") treesEncountered++;

        xPos += 3; // right 3
    } // down 1

    return treesEncountered;
}

function partTwo() {
    const mapRows = input.split("\n");
    const rowLength = mapRows[0].length;
    const moves = [[1, 1], [3, 1], [5, 1], [7, 1], [1, 2]]; // Each item is [xMove, yMove]

    // (0, 0) is in the top left corner, starting position
    let allTreesEncountered = [];
    for (let i = 0; i < moves.length; i++) {
        const xMove = moves[i][0];
        const yMove = moves[i][1];
        let treesEncountered = 0;
        let xPos = 0;
        for (let yPos = 0; yPos < mapRows.length; yPos += yMove) {
            if (mapRows[yPos].substr(xPos % rowLength, 1) == "#") treesEncountered++;

            xPos += xMove; // right xMove
        } // down yMove
        allTreesEncountered[i] = treesEncountered;
    }

    return allTreesEncountered.reduce((a, b) => a * b);
}

